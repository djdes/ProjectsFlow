// Календарная дата в фиксированной зоне продукта (Europe/Moscow).
//
// Почему не UTC: сервер живёт в UTC, а пользователи — в Москве (UTC+3). Наивное
// `new Date().toISOString().slice(0, 10)` с 21:00 до 24:00 МСК отдаёт ВЧЕРАШНЮЮ дату
// с точки зрения пользователя: задача, созданная в 22:30 «на сегодня», получила бы
// вчерашний срок и сразу выглядела просроченной.
//
// Зона зашита константой, а не берётся из профиля пользователя: продукт одноязычный/
// однорегиональный, и та же константа уже используется в планировщиках (EOD-напоминания,
// дайджесты, commit-sync) — «сегодня» обязано совпадать во всех этих местах, иначе
// напоминание «просрочено» разъедется с датой, проставленной при создании задачи.
const PRODUCT_TIME_ZONE = 'Europe/Moscow';

// en-CA форматирует ровно как YYYY-MM-DD — тот же формат, в котором deadline хранится в БД.
const FORMATTER = new Intl.DateTimeFormat('en-CA', {
  timeZone: PRODUCT_TIME_ZONE,
  year: 'numeric',
  month: '2-digit',
  day: '2-digit',
});

/** Календарная дата момента `at` в зоне продукта, 'YYYY-MM-DD'. */
export function moscowDateOnly(at: Date): string {
  return FORMATTER.format(at);
}
