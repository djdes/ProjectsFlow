-- 006: secrets хранятся теперь plaintext, без AES-GCM шифрования.
-- Причина: master-ключ жил в .env, потеря/перезапись = безвозвратная утрата всех
-- значений. Для one-tenant приложения на собственном VPS защита от утёкшего
-- DB-дампа отдельно от сервера не оправдывает риск потери данных.
--
-- Существующие записи зашифрованы старым ключом — расшифровать нельзя, удаляем.
-- Колонка `encrypted` переименована в `value` для честности.

TRUNCATE TABLE secrets;

ALTER TABLE secrets CHANGE encrypted `value` VARCHAR(2000) NOT NULL;
