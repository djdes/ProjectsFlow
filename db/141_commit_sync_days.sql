-- Дни недели для per-project сверки коммитов.
--
-- Раньше время и дни commit-sync жили только на уровне пространства (один набор на всё
-- пространство). Теперь у каждого проекта своё расписание сверки: время (commit_sync_hour/minute
-- уже были) плюс дни недели — эта колонка.
--
-- Формат: JSON-массив чисел 0..6 (0 — воскресенье, как getUTCDay). NULL трактуется как «каждый
-- день» — это и дефолт: пользователь просил «по умолчанию каждый день в 17:00», и существующим
-- 127 проектам не нужен бэкфилл (NULL уже означает все дни).
ALTER TABLE project_automation
  ADD COLUMN commit_sync_days LONGTEXT NULL AFTER commit_sync_minute;
