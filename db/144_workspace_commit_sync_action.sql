-- 144_workspace_commit_sync_action.sql
-- Режим сверки коммитов на уровне пространства.
--
-- В окне настроек пространства (карточка «Сверка коммитов») появился выбор режима:
--   'auto'    — переносить задачи автоматически по совпавшим коммитам;
--   'propose' — только оповещать (предлагать закрыть), ничего не двигая.
--
-- Раньше режим жил только per-project (project_automation.commit_sync_action, db/101). Теперь
-- он поднят на уровень пространства, чтобы его помнить между визитами и чтобы мастер-действие
-- «Применить ко всем проектам» проставляло его во все проекты разом.
--
-- Дефолт 'propose' — безопасный (ничего не переносим без явного включения авто-режима).
ALTER TABLE workspace_assignee_digest_settings
  ADD COLUMN commit_sync_action ENUM('propose','auto') NOT NULL DEFAULT 'propose'
  AFTER commit_sync_minute;
