-- 143_commit_sync_batching.sql
-- Батчинг ежедневной сверки коммитов: несколько проектов, у которых ОДИНАКОВЫ Telegram-группа,
-- дата, час и минута сверки, объединяются в ОДНО свёрнутое сообщение вместо N отдельных.
--
-- Раньше CompleteCommitSyncJob на каждый завершившийся job слал в группу пространства отдельное
-- сообщение. Теперь job'ы одного тика планировщика для одной группы+времени помечаются общим
-- batch_key; последний завершившийся job батча («последний гасит свет») собирает результаты всех
-- проектов и шлёт одно сообщение.
--
-- batch_key
--   Общий ключ батча '<groupChatId>:<YYYY-MM-DD>:<HH>:<MM>'. Проставляет планировщик всем job'ам
--   одного тика для одной группы+времени. NULL — одиночная доставка (ручная «Сверить сейчас»
--   шлётся сразу и не ждёт батч).
-- review_json
--   Per-job готовый payload сводки (chatId + проект + режим auto/propose + строки задач),
--   сериализуется при завершении job'а. Из этих payload'ов сборщик агрегирует одно сообщение.
--   Чистый проект (нечего показывать) пишет NULL и в дайджест не попадает.
-- batch_flushed_at
--   Момент, когда единственное сообщение батча уже отправлено. Атомарный выбор сборщика: весь
--   батч гасится одним UPDATE (SET batch_flushed_at на все строки при отсутствии незавершённых
--   job'ов и NULL-флаге) — защита от двойной отправки при гонке параллельных завершений.
ALTER TABLE commit_sync_jobs
  ADD COLUMN batch_key VARCHAR(120) NULL AFTER action,
  ADD COLUMN review_json MEDIUMTEXT NULL AFTER matches_json,
  ADD COLUMN batch_flushed_at TIMESTAMP NULL AFTER finished_at,
  ADD INDEX idx_csj_batch_key (batch_key);
