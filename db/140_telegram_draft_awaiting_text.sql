-- Правка текста задачи в карточке Telegram-конструктора.
--
-- Карточка позволяла менять проект, ответственного, колонку и срок, но не сам текст: если AI
-- сформулировал задачу неточно, оставалось только отменить и надиктовать заново.
--
-- Здесь хранится «какой сегмент ждёт новый текст»: пользователь нажал «Текст», бот попросил
-- прислать формулировку ответом, и следующее его сообщение уходит не в новую задачу, а в этот
-- сегмент. NULL — обычное состояние, ничего не ждём.
--
-- Индекс сегмента, а не флаг: у многосегментной карточки правят конкретную задачу из пачки.
-- -1 зарезервирован под ручной (не-AI) черновик, где сегментов нет и текст один.
ALTER TABLE telegram_task_drafts
  ADD COLUMN awaiting_text_seg INT NULL AFTER target_status;
